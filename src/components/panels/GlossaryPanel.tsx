const ROW_LABELS: [string, string[]][] = [
  ["あ行", ["あ", "い", "う", "え", "お"]],
  ["か行", ["か", "き", "く", "け", "こ", "が", "ぎ", "ぐ", "げ", "ご"]],
  ["さ行", ["さ", "し", "す", "せ", "そ", "ざ", "じ", "ず", "ぜ", "ぞ"]],
  ["た行", ["た", "ち", "つ", "て", "と", "だ", "ぢ", "づ", "で", "ど"]],
  ["な行", ["な", "に", "ぬ", "ね", "の"]],
  ["は行", ["は", "ひ", "ふ", "へ", "ほ", "ば", "び", "ぶ", "べ", "ぼ", "ぱ", "ぴ", "ぷ", "ぺ", "ぽ"]],
  ["ま行", ["ま", "み", "む", "め", "も"]],
  ["や行", ["や", "ゆ", "よ"]],
  ["ら行", ["ら", "り", "る", "れ", "ろ"]],
  ["わ行", ["わ", "を", "ん"]],
];

function rowLabelOf(yomi: string): string {
  const first = yomi.charAt(0);
  const found = ROW_LABELS.find(([, chars]) => chars.includes(first));
  return found ? found[0] : "その他";
}

const GLOSSARY = [...RAW_GLOSSARY].sort((a, b) => a.yomi.localeCompare(b.yomi, "ja"));

export default function GlossaryPanel() {
  const [query, setQuery] = useState("");
  const q = query.trim().toLowerCase();
  const list = GLOSSARY.filter((g) => !q || g.term.toLowerCase().includes(q) || g.desc.toLowerCase().includes(q));

  let lastRow = "";

  return (
    <div className="card">
      <h2 className="section-title">📖 IRIAM用語集</h2>
      <p className="lead">配信を始める前に出会う独自用語を五十音順にまとめました。</p>
      <input
        type="text"
        value={query}
        onChange={(e) => setQuery(e.target.value)}
        placeholder="用語を検索（例：ダイヤ）"
        style={{ width: "100%", marginBottom: 14 }}
      />
      {list.length === 0 ? (
        <div className="empty-note">該当する用語が見つかりませんでした。</div>
      ) : (
        list.map((g) => {
          const row = rowLabelOf(g.yomi);
          const showHeader = row !== lastRow;
          lastRow = row;
          return (
            <div key={g.term}>
              {showHeader && (
                <div
                  style={{
                    marginTop: 18,
                    marginBottom: 6,
                    fontWeight: 700,
                    fontSize: "0.85em",
                    letterSpacing: "0.05em",
                    opacity: 0.6,
                  }}
                >
                  {row}
                </div>
              )}
              <div className="glossary-item">
                <div className="term">{g.term}</div>
                <div className="desc" style={{ whiteSpace: "pre-line" }}>{g.desc}</div>
              </div>
            </div>
          );
        })
      )}
      <div className="disclaimer">
        <b>おことわり：</b>各用語の説明はIRIAM公式FAQや配信者向け情報をもとにした要約です。一部は公式非公開・推定値の情報も含まれるため、正式な仕様は必ずアプリ内の公式ヘルプでご確認ください。
      </div>
    </div>
  );
}
